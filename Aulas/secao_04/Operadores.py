# Operadores Matemáticos
print(10 + 2)  # Soma
print(10 - 2)  # Subtração
print(10 / 2)  # Divisão
print(10 * 2)  # Multiplicação
print(10 ** 2)  # Exponenciação
print(11 // 2)  # Divisão sem resto
print(10 % 3)  # Diferença da divisão

# Operadores Lógicos
print(10 == 2)  # Igual a
print(10 != 2)  # Diferente de
print(10 > 2)  # Maior que
print(10 < 2)  # Menor que
print(10 >= 2)  # Maior ou igual que
print(10 >= 2)  # Menor ou igual que


